public class ForLoop {
    public static void main(String[] args){
        //1.Initialize
        //2.Condition
        //3.Update/Modify
        for (int i=1; i<5; i++){
            //loop statement
            System.out.println("Loop" +1);
        }

        //Dcrement
        int dec;
        for(dec=5; dec>=1; dec -=2){
            System.out.println("Loop dec by two" +dec);
        }
    }
    
}
